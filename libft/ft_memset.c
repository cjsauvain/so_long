#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*cast;

	i = 0;
	cast = s;
	while (i < n)
	{
		cast[i] = c;
		i++;
	}
	return (s);
}
