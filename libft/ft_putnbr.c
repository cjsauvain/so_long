#include "libft.h"

int	ft_putnbr(int n, int i)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		i += 11;
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
		i++;
	}
	if (n >= 10)
		i = ft_putnbr(n / 10, i);
	if (n >= 0)
		i += ft_putchar((n % 10) + '0');
	return (i);
}
